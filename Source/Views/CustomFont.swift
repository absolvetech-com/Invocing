
import UIKit

extension UIFont {

    
   static func TitleFont() -> UIFont {
        let TitleFont = UIFont.systemFont(ofSize: 22, weight: .semibold)
        return TitleFont
    }
    
 static   func SubTitleFont() -> UIFont
    {
        let SubTitleFont = UIFont.systemFont(ofSize: 18, weight: .regular)
        return SubTitleFont
    }
    
    static   func TitleRegular() -> UIFont
    {
        let TitleRegular = UIFont.systemFont(ofSize: 16, weight: .regular)
        return TitleRegular
    }
    
    
    
    
 static   func DateFont() -> UIFont
    {
        let DateFont = UIFont.systemFont(ofSize: 18, weight: .regular)
        return DateFont
    }
    
  static  func PriceFont() -> UIFont {
        let PriceFont = UIFont.systemFont(ofSize: 18, weight: .regular)
        return PriceFont
    }
  static  func StatusFont() -> UIFont {
        let StatusFont = UIFont.systemFont(ofSize: 18, weight: .medium)
        return StatusFont
    }
    
    static  func LightTitleFont() -> UIFont {
        let StatusFont = UIFont.systemFont(ofSize: 18, weight: .light)
        return StatusFont
    }
    
    static  func LightStatusFont() -> UIFont {
        let StatusFont = UIFont.systemFont(ofSize: 13, weight: .light)
        return StatusFont
    }
    
    static  func LightSmaillFont() -> UIFont {
        let StatusFont = UIFont.systemFont(ofSize: 12, weight: .light)
        return StatusFont
    }
    
    static  func RegularStatusFont() -> UIFont {
        let StatusFont = UIFont.systemFont(ofSize: 13, weight: .regular)
        return StatusFont
    }
    
    
    static   func TabBatTitleFont() -> UIFont
    {
        let TabBatTitleFont = UIFont.systemFont(ofSize: 10, weight: .regular)
        return TabBatTitleFont
    }
   
}
